num_a = int(input('Digite seu primeiro número: '))
num_b = int(input('Digite seu segundo número: '))

soma = num_a + num_b
subitração = num_a - num_b
multiplicação = num_a * num_b
divisão = num_a / num_b

print(f'Resultado da SOMA: {soma} ')
print(f'Resultado da subitração: {subitração} ')
print(f'Resultado da multiplicação: {multiplicação} ')
print(f'Resultado da divisão: {divisão} ')
