tipo_animal = input('Seu animal favorito é mamífero ou réptil?')

if tipo_animal.lower() == 'mamífero':
    print('Seu animal favorito é um cachorro!')

elif tipo_animal.lower() == 'réptil':
    print('Seu animal favorito é uma Iguana!')

else:
    print('Não consigo adivinhar seu animal favorito!')
